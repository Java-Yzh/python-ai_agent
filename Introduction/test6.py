# 列表（list）特性：
# 1. 有序性：元素按插入顺序排列，每个元素有对应索引（从 0 开始）
# 2. 可变性：创建后可以修改、添加、删除元素，对象内存地址不变
# 3. 支持任意类型：同一列表可混合存放不同类型，如 [1, "hello", 3.14, True]
# 4. 支持嵌套：列表中可以包含其他列表，如 [[1,2], [3,4]]
# 5. 索引与切片：
#      正向索引：fruits[0]    反向索引：fruits[-1]
#      切片：fruits[1:3]      步长切片：fruits[::2]
# 6. 动态增删：
#      append(x)   末尾添加   | insert(i, x)  指定位置插入
#      extend(lst) 合并列表   | remove(x)     删除第一个匹配元素
#      pop(i)      删除并返回 | clear()       清空列表
# 7. 排序与反转：sort() 原地排序 / reverse() 原地反转 / sorted() 返回新列表
# 8. 运算符操作：
#      [1,2] + [3,4]  拼接   | [0] * 3  重复   | 3 in [1,2,3]  成员判断
# 9. 长度与统计：len() 元素个数 / count(x) 出现次数
# 10. 列表推导式：squares = [x**2 for x in range(5)]  → [0, 1, 4, 9, 16]
#
# 总结：列表是 Python 中最常用、最灵活的有序可变序列，
#       支持任意类型混合、动态增删、索引切片，适合绝大多数"一组数据"的场景。

# 列表
fruits = ["苹果", "香蕉", "橘子", "葡萄"]

print(fruits[0])        # 第 1 个（编号从 0 开始）
print(fruits[-1])       # 倒数第 1 个
print(fruits[1:3])      # 第 2 到第 3 个（不含第 4 个）

fruits.append("香蕉")
fruits.remove("香蕉")
fruits[0] = "梨"
print(fruits)
print("一共有" + str(len(fruits)) + "个水果。")