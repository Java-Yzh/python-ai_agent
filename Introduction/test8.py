# 元组（tuple）特性：
# 1. 有序性：元素按插入顺序排列，每个元素有对应索引（从 0 开始）
# 2. 不可变性：创建后不能修改、添加或删除元素，保证数据安全
# 3. 支持任意类型：同一元组可混合存放不同类型，如 (1, "hello", 3.14)
# 4. 支持嵌套：元组中可以包含其他元组或列表，如 ((1,2), (3,4))
# 5. 索引与切片：
#      t[0]       正向索引    | t[-1]      反向索引
#      t[1:3]     切片        | t[::2]     步长切片
# 6. 创建方式：
#      (1, 2, 3)      标准写法  | 1, 2, 3       省略括号
#      (42,)           单元素必须加逗号 | ()         空元组
# 7. 拆包（解包）：
#      x, y, z = (1, 2, 3)    变量个数要匹配
#      first, *rest = (1, 2, 3, 4)  用 * 收集剩余元素
# 8. 运算符操作：
#      (1,2) + (3,4)  拼接   | (0,) * 3  重复   | 3 in (1,2,3)  成员判断
# 9. 长度与统计：len() 元素个数 / count(x) 出现次数 / index(x) 查找位置
# 10. 可作为字典的键：元组可哈希，能当 dict 的键；列表不行
# 11. 与列表互转：
#      list((1,2,3))  → [1, 2, 3]   | tuple([1,2,3])  → (1, 2, 3)
#
# 总结：元组是 Python 中的有序不可变序列，
#       比列表更节省内存、访问更快，适合存储不应被修改的固定数据。

# 元组
colors = ("红", "绿", "蓝")

print(colors[0])          # 第 1 个
print(colors[-1])         # 倒数第 1 个
print(colors[0:2])        # 第 1 到第 2 个

# 拆包
r, g, b = colors
print("r =", r, "g =", g, "b =", b)

# 元组不可修改，以下操作会报错：
# colors[0] = "黄"        # TypeError: 'tuple' object does not support item assignment

print("一共有" + str(len(colors)) + "种颜色。")